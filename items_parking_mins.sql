-- 주차요금 시간당 단가 비교 — items 에 주차시간(분) 컬럼 추가  b515
--
--   목적: 주차요금은 주차시간에 따라 총액이 달라져 단순 가격 비교가 왜곡됨.
--         주차시간(분)을 보존 → 가격비교에서 ¥/시간(시간당 단가)으로 공정 비교.
--   - mins : 주차시간(분). 주차요금 품목에만 채워짐(그 외 NULL).
--
--   ⚠️ 미실행 시에도 저장은 정상 동작함(클라가 mins 컬럼 없으면 자동 제외 후 재시도).
--      단, 컬럼이 없으면 주차시간이 영구 보존되지 않아 새로고침 후 시간당 비교가 사라짐.
--   Supabase SQL Editor 에서 실행.
-- ════════════════════════════════════════════════════════════════════════════

alter table public.items add column if not exists mins integer;
